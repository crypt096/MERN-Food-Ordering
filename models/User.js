const mongoose = require("mongoose");
const Schema = mongoose.Schema;

// Creating schema
const UserSchema = new Schema({
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true,
    unique: true
  },
  password: {
    type: String,
    required: true
  },
  register_date: {
    type: Date,
    default: Date.now
  },
  firstName: {
    type: String,
    required: true
  },
  lastName: {
    type: String,
    required: true
  },
  address: {
    type: String
  },
  favoriteFood: {
    type: String
  }
});

module.exports = User = mongoose.model("user", UserSchema);
